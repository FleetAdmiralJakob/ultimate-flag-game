export const flagLength = flags.length;

export const AllFlags = observer(() => {
  const [count, setCount] = React.useState(0);

  const chosenFlags = getRandomFlags(flags, 4);
  const flagIndex = Math.floor(Math.random() * 4);

  React.useEffect(() => {
    const handleKeyDown = (event: KeyboardEvent) => {
      if (event.key === "1") {
        handleButtonClick(0);
      } else if (event.key === "2") {
        handleButtonClick(1);
      } else if (event.key === "3") {
        handleButtonClick(2);
      } else if (event.key === "4") {
        handleButtonClick(3);
      }
    };

    window.addEventListener("keydown", handleKeyDown);
    return () => {
      window.removeEventListener("keydown", handleKeyDown);
    };
  }, [count]);

  const handleButtonClick = (index: number) => {
    if (flagIndex === index) {
      setCount(count + 1);
    } else {
      setCount(0);
    }
  };

  const progress = Math.min((count / flagLength) * 100, 100);

  return (
    <div className="flex flex-col gap-10 w-full h-screen items-center justify-center">
      {count}
      <div className="w-72 bg-gray-200 rounded-full h-4 overflow-hidden">
        <div
          className="bg-blue-600 h-4 rounded-full"
          style={{ width: `${progress}%` }}
        ></div>
      </div>
      <img
        src={chosenFlags[flagIndex].path}
        className="h-32 w-auto shadow-2xl rounded"
      />
      <div className="grid grid-cols-2 grid-rows-2 gap-4">
        <button
          onClick={() => handleButtonClick(0)}
          className="bg-slate-200 w-36 rounded shadow-2xl h-20"
        >
          1{" "}
          {language$.get() === "english"
            ? chosenFlags[0].name
            : chosenFlags[0].germanName}
        </button>
        <button
          onClick={() => handleButtonClick(1)}
          className="bg-slate-200 w-36 rounded shadow-2xl h-20"
        >
          2{" "}
          {language$.get() === "english"
            ? chosenFlags[1].name
            : chosenFlags[1].germanName}
        </button>
        <button
          onClick={() => handleButtonClick(2)}
          className="bg-slate-200 w-36 rounded shadow-2xl h-20"
        >
          3{" "}
          {language$.get() === "english"
            ? chosenFlags[2].name
            : chosenFlags[2].germanName}
        </button>
        <button
          onClick={() => handleButtonClick(3)}
          className="bg-slate-200 w-36 rounded shadow-2xl h-20"
        >
          4{" "}
          {language$.get() === "english"
            ? chosenFlags[3].name
            : chosenFlags[3].germanName}
        </button>
      </div>
    </div>
  );
});
